    private void txtSchstuIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSchstuIDKeyReleased
 Pattern pattern = Pattern.compile("^\\s*$");
        if (!txtSchstuID.getText().isEmpty() && !pattern.matcher(txtSchstuID.getText()).matches()) {

            String searchText = txtSchstuID.getText().trim();

            String columnName = "Subject_ID";

            Connection conn = null;
            try {
                conn = new DBConnection().getConnection();
                String sql = "SELECT * FROM subject WHERE " + columnName + " LIKE ?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, "%" + searchText + "%");
                ResultSet rs = pst.executeQuery();

                DefaultTableModel model = (DefaultTableModel) rSTableMetro1.getModel();
                model.setRowCount(0); // Clear previous search results
                while (rs.next()) {
                    String Subject_ID = rs.getString("Subject_ID");
                    String Subject_Name = rs.getString("Subject_Name");
                    String Subject_Fee = rs.getString("Subject_Fee");
                    String Grade = rs.getString("Grade");
                    
                    
                  
                    model.addRow(new Object[]{Subject_ID, Subject_Name, Subject_Fee, Grade});

                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {

           table_update();
        }
    }//GEN-LAST:event_txtSchstuIDKeyReleased

