    public final void AutoID() {
        Connection conn = null;
        try {
            conn = new DBConnection().getConnection();
            java.sql.Statement s = conn.createStatement();
            rs = s.executeQuery("SELECT MAX(Subject_ID) FROM subject");
            rs.next();
            String maxSubID = rs.getString("MAX(Subject_ID)");
            if (maxSubID == null) {
                lblSubID.setText("sub00001");
            } else {
                int id = Integer.parseInt(maxSubID.substring(3));
                id++;
                String newSubID = String.format("sub%05d", id);
                lblSubID.setText(newSubID);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Subjects.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void table_update() {
        int CC;
        Connection conn = null;
        try {
            conn = new DBConnection().getConnection();

            pst = conn.prepareStatement("SELECT * FROM subject");
            ResultSet Rs = pst.executeQuery();
            java.sql.ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) rSTableMetro1.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("Subject_ID"));
                    v2.add(Rs.getString("Subject_Name"));
                    v2.add(Rs.getString("Subject_Fee"));
                    v2.add(Rs.getString("Grade"));

                }
                DFT.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Subject.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
