    private void btnAddSubActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSubActionPerformed

        String Subject_ID = lblSubID.getText();
        String Subject_Name = txtSubName.getText();
        String Subject_Fee = txtSubFee.getText();
        String Grade = txtGrade.getText();

        SubjectBean subBean = new SubjectBean();
        subBean.setSubject_ID(Subject_ID);
        subBean.setSubject_Name(Subject_Name);
        subBean.setSubject_Fee(Subject_Fee);
        subBean.setGrade(Grade);

        boolean addSubject = new SubjectLogic().addSubject(subBean);

        if (addSubject) {
            JOptionPane.showMessageDialog(this, "successfully ADD");
            AutoID();
            txtSubName.setText(null);
            txtSubFee.setText(null);
            txtGrade.setText(null);

        } else {
            JOptionPane.showMessageDialog(this, "Unable to ADD subject");
        }
        table_update();

    }//GEN-LAST:event_btnAddSubActionPerformed

